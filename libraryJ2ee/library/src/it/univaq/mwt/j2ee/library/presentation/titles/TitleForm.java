package it.univaq.mwt.j2ee.library.presentation.titles;

import org.apache.struts.validator.ValidatorForm;

public class TitleForm extends ValidatorForm {
	
	private Long id;
	private String name;
	private String author;
	private String description;
	private String isbn;
	private String publicationYear;
	private String editor;
	private long titleKindId;
	
	
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAuthor() {
		return author;
	}
	public void setAuthor(String author) {
		this.author = author;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getIsbn() {
		return isbn;
	}
	public void setIsbn(String isbn) {
		this.isbn = isbn;
	}
	public String getPublicationYear() {
		return publicationYear;
	}
	public void setPublicationYear(String publicationYear) {
		this.publicationYear = publicationYear;
	}
	public String getEditor() {
		return editor;
	}
	public void setEditor(String editor) {
		this.editor = editor;
	}
	public long getTitleKindId() {
		return titleKindId;
	}
	public void setTitleKindId(long titleKindId) {
		this.titleKindId = titleKindId;
	}

	
	

}
